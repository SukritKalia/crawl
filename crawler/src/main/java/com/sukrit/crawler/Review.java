package com.sukrit.crawler;

public class Review {
private String reviewerFirstname;
private String reviewerLastname;
private String reviewerJobTitle;
private String reviewerCompanyName;
private ProfessionalPortal professionalPortal;
private String reviewerEmail;

public String getReviewerFirstname() {
	return reviewerFirstname;
}
public void setReviewerFirstname(String reviewerFirstname) {
	this.reviewerFirstname = reviewerFirstname;
}
public String getReviewerLastname() {
	return reviewerLastname;
}
public void setReviewerLastname(String reviewerLastname) {
	this.reviewerLastname = reviewerLastname;
}
public String getReviewerJobTitle() {
	return reviewerJobTitle;
}
public void setReviewerJobTitle(String reviewerJobTitle) {
	this.reviewerJobTitle = reviewerJobTitle;
}




public String getReviewerEmail() {
	return reviewerEmail;
}
public void setReviewerEmail(String reviewerEmail) {
	this.reviewerEmail = reviewerEmail;
}
public String getReviewerCompanyName() {
	return reviewerCompanyName;
}
public void setReviewerCompanyName(String reviewerCompanyName) {
	this.reviewerCompanyName = reviewerCompanyName;
}
public ProfessionalPortal getProfessionalPortal() {
	return professionalPortal;
}
public void setProfessionalPortal(ProfessionalPortal professionalPortal) {
	this.professionalPortal = professionalPortal;
}



}
